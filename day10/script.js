input = input.split('\n')
let matches = {
  '(' : ')',
  '[' : ']',
  '<' : '>',
  '{' : '}'
}

let scoring1 = {
  ')' : 3,
  ']' : 57,
  '}' : 1197,
  '>' : 25137
}

let scoring2 = {
  ')' : 1,
  ']' : 2,
  '}' : 3,
  '>' : 4
}

const dayOne = () => {
  let errors = [];
  for(row of input) {
    let done = false;
    row = row.split('')
    let opened = [];
    for(char of row) {
      if(Object.keys(matches).includes(char)){
        opened.push(char)
        continue;
      } else {
        let recentOpen = opened.pop()
        if(char !== matches[recentOpen]){
          errors.push(char)
          done = true;
        }
      }
      if(done) break;
    }
  }

  const score = errors.map( (curr) => scoring1[curr]).reduce( (total, curr) => total + curr)
  console.log(score);
}

const dayTwo = () => {
  let incomplete = [];
  for(row of input) {
    let error = false;
    row = row.split('')
    let opened = [];
    for(char of row) {
      if(Object.keys(matches).includes(char)){
        opened.push(char)
        continue;
      } else {
        let recentOpen = opened.pop()
        if(char !== matches[recentOpen]) error = true;
      }
      if(error) {
        break;
      }
    }
    if(!error) incomplete.push(opened)
  }
  
  scores = incomplete.map( (arr) => {
    total = 0;
    arr = arr.reverse()
    for(char of arr){
      total *= 5
      total += scoring2[matches[char]]
    }
    return total;
  })
  scores = scores.sort((a, b) => a - b)
  return console.log(scores[scores.length / 2 - 0.5]);
}

